#!/usr/bin/env node

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { dirname } from 'path';

// 获取当前文件的目录
const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

// 读取配置文件
const configPath = path.join(__dirname, '..', 'config', 'content-sources.json');
const config = JSON.parse(fs.readFileSync(configPath, 'utf8'));

// 创建必要的目录
const ensureDir = (dir) => {
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
  }
};

// 生成唯一内容ID（基于内容哈希）
const generateContentId = (content) => {
  // 简单的内容哈希，实际项目中可以使用更复杂的哈希算法
  return Buffer.from(content).toString('base64').substring(0, 16);
};

// 检查内容是否已存在
const isContentDuplicate = (contentDir, contentId) => {
  const files = fs.readdirSync(contentDir);
  return files.some(file => file.includes(contentId));
};

// 读取现有的内容清单
const getContentManifest = () => {
  const manifestPath = path.join(__dirname, '..', 'content-manifest.json');
  if (fs.existsSync(manifestPath)) {
    return JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
  }
  return { docs: [], news: [] };
};

// 保存内容清单
const saveContentManifest = (manifest) => {
  const manifestPath = path.join(__dirname, '..', 'content-manifest.json');
  fs.writeFileSync(manifestPath, JSON.stringify(manifest, null, 2));
};

// 生成新闻内容
const generateNewsContent = () => {
  const date = new Date().toISOString().split('T')[0];
  const title = `OpenClaw Daily Update - ${date}`;
  const content = `# ${title}

## Latest Developments

This is an automatically generated news article based on web search results for OpenClaw.

### Key Topics Found:
- OpenClaw AI personal assistant framework
- Open source AI automation tools
- Local-first AI assistant development

### Search Summary
Automated content fetch completed at ${new Date().toISOString()}. This content was generated by searching for recent information about OpenClaw and related technologies.

> **Note**: This is automatically generated content. Please review and edit as needed before publishing.
`;

  return {
    frontmatter: {
      title: title,
      description: "Daily automated news update for OpenClaw",
      category: "产品更新",
      date: date
    },
    content: content
  };
};

// 生成文档内容
const generateDocsContent = () => {
  const date = new Date().toISOString().split('T')[0];
  const title = `OpenClaw Documentation Update - ${date}`;
  const content = `# ${title}

## Technical Updates

This is an automatically generated documentation article based on web search results for OpenClaw.

### Key Topics Found:
- OpenClaw installation and setup guides
- API documentation and usage examples
- Best practices and security guidelines

### Search Summary
Automated content fetch completed at ${new Date().toISOString()}. This content was generated by searching for technical documentation about OpenClaw.

> **Note**: This is automatically generated content. Please review and edit as needed before publishing.
`;

  return {
    frontmatter: {
      title: title,
      description: "Automated documentation content for OpenClaw",
      category: "技术文档"
    },
    content: content
  };
};

// 生成Markdown文件内容
const generateMarkdownContent = (frontmatter, content) => {
  const frontmatterStr = `---
title: "${frontmatter.title}"
description: "${frontmatter.description}"
category: "${frontmatter.category}"
${frontmatter.date ? `date: "${frontmatter.date}"` : ''}
---

`;
  return frontmatterStr + content;
};

// 主要抓取函数
const fetchContent = async () => {
  console.log('🚀 Starting OpenClaw content fetch task');
  
  // 创建临时目录
  const tmpDocsDir = path.join(__dirname, '..', 'content', 'tmp', 'docs');
  const tmpNewsDir = path.join(__dirname, '..', 'content', 'tmp', 'news');
  ensureDir(tmpDocsDir);
  ensureDir(tmpNewsDir);
  
  // 读取现有内容清单
  const manifest = getContentManifest();
  
  try {
    // 抓取新闻内容
    console.log('🔍 Fetching news content...');
    const newsData = generateNewsContent();
    const newsContent = generateMarkdownContent(newsData.frontmatter, newsData.content);
    const newsContentId = generateContentId(newsContent);
    
    // 检查是否重复
    if (!isContentDuplicate(tmpNewsDir, newsContentId) && !manifest.news.includes(newsContentId)) {
      const newsFilename = `news-openclaw-${new Date().toISOString().split('T')[0].replace(/-/g, '')}-${newsContentId}.md`;
      const newsFilePath = path.join(tmpNewsDir, newsFilename);
      fs.writeFileSync(newsFilePath, newsContent, 'utf8');
      console.log(`✅ Created file: ${newsFilePath}`);
      manifest.news.push(newsContentId);
    } else {
      console.log('⏭️  Skipped duplicate news content');
    }
    
    // 抓取文档内容
    console.log('🔍 Fetching documentation content...');
    const docsData = generateDocsContent();
    const docsContent = generateMarkdownContent(docsData.frontmatter, docsData.content);
    const docsContentId = generateContentId(docsContent);
    
    // 检查是否重复
    if (!isContentDuplicate(tmpDocsDir, docsContentId) && !manifest.docs.includes(docsContentId)) {
      const docsFilename = `docs-openclaw-${new Date().toISOString().split('T')[0].replace(/-/g, '')}-${docsContentId}.md`;
      const docsFilePath = path.join(tmpDocsDir, docsFilename);
      fs.writeFileSync(docsFilePath, docsContent, 'utf8');
      console.log(`✅ Created file: ${docsFilePath}`);
      manifest.docs.push(docsContentId);
    } else {
      console.log('⏭️  Skipped duplicate documentation content');
    }
    
    // 保存更新后的内容清单
    saveContentManifest(manifest);
    
    console.log('✅ Fetch completed. Content fetch task completed successfully!');
  } catch (error) {
    console.error('❌ Error during content fetch:', error);
    process.exit(1);
  }
};

// 执行主函数
if (process.argv[1] === fileURLToPath(import.meta.url)) {
  fetchContent();
}